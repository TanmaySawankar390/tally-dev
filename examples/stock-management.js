import TallyPrimeSDK from '../src/index.js';

/**
 * Stock Management Example
 * 
 * This example demonstrates how to:
 * 1. Create stock items
 * 2. Fetch stock item details
 * 3. Update stock items
 * 4. Get stock item lists
 * 5. Check stock balances and movements
 * 6. Generate stock summaries
 */

async function stockManagementExample() {
    console.log('üì¶ Stock Management Examples');
    console.log('='.repeat(50));

    const tally = new TallyPrimeSDK();

    try {
        // Test connection first
        const isConnected = await tally.testConnection();
        if (!isConnected) {
            throw new Error('Cannot connect to TallyPrime. Please ensure it is running.');
        }

        console.log('‚úÖ Connected to TallyPrime');

        // Example 1: Create stock items using the quick method
        console.log('\\n1Ô∏è‚É£ Creating stock items...');
        
        const stockItemA = await tally.createStockItem('Widget A', 'Raw Materials', 'Nos', {
            alias: 'WGT-A',
            openingBalance: {
                quantity: 100,
                rate: 50.00,
                value: 5000.00
            }
        });

        console.log('‚úÖ Stock item created:', stockItemA.message);
        console.log('üìù Stock item name:', stockItemA.stockItemName);

        // Example 2: Create multiple stock items using the service directly
        console.log('\\n2Ô∏è‚É£ Creating multiple stock items...');
        
        const stockItemsToCreate = [
            {
                name: 'Widget B',
                parent: 'Raw Materials',
                baseUnits: 'Kg',
                alias: 'WGT-B',
                openingBalance: {
                    quantity: 50,
                    rate: 75.00,
                    value: 3750.00
                }
            },
            {
                name: 'Finished Product X',
                parent: 'Finished Goods',
                baseUnits: 'Nos',
                alias: 'FP-X',
                openingBalance: {
                    quantity: 25,
                    rate: 200.00,
                    value: 5000.00
                }
            },
            {
                name: 'Packaging Material',
                parent: 'Raw Materials',
                baseUnits: 'Mtr',
                alias: 'PKG-MAT'
            },
            {
                name: 'Semi Finished Y',
                parent: 'Work in Progress',
                baseUnits: 'Nos',
                alias: 'SF-Y',
                openingBalance: {
                    quantity: 15,
                    rate: 120.00,
                    value: 1800.00
                }
            }
        ];

        for (const stockData of stockItemsToCreate) {
            try {
                const result = await tally.stock.createStockItem(stockData);
                console.log(`‚úÖ Created: ${result.stockItemName}`);
            } catch (error) {
                console.log(`‚ö†Ô∏è  Could not create ${stockData.name}: ${error.message}`);
            }
        }

        // Example 3: Fetch stock item details
        console.log('\\n3Ô∏è‚É£ Fetching stock item details...');
        
        try {
            const stockDetails = await tally.stock.fetchStockItem('Widget A', {
                includeBalance: true,
                asOn: '31-Dec-2023'
            });

            console.log('‚úÖ Stock item details retrieved:');
            console.log('üìù Name:', stockDetails.data.name);
            console.log('üè¢ Parent:', stockDetails.data.parent);
            console.log('üîñ Alias:', stockDetails.data.alias);
            console.log('üìè Base Units:', stockDetails.data.baseUnits);
            console.log('üí∞ Opening Balance:');
            console.log('   üìä Quantity:', stockDetails.data.openingBalance?.quantity || 0);
            console.log('   üíµ Rate:', stockDetails.data.openingBalance?.rate || 0);
            console.log('   üí∞ Value:', stockDetails.data.openingBalance?.value || 0);
        } catch (error) {
            console.log(`‚ö†Ô∏è  Could not fetch stock details: ${error.message}`);
        }

        // Example 4: Get stock item list with filters
        console.log('\\n4Ô∏è‚É£ Getting stock item list...');
        
        try {
            const stockList = await tally.stock.getStockItemList({
                group: 'Raw Materials',
                activeOnly: true,
                nameContains: 'Widget'
            });

            console.log(`‚úÖ Found ${stockList.count} stock item(s) matching criteria:`);
            stockList.data.forEach((item, index) => {
                console.log(`   ${index + 1}. ${item.name} (${item.parent}) - ${item.baseUnits}`);
                if (item.closingBalance) {
                    console.log(`      üìä Closing: ${item.closingBalance.quantity} units, ‚Çπ${item.closingBalance.value}`);
                }
            });
        } catch (error) {
            console.log(`‚ö†Ô∏è  Could not fetch stock list: ${error.message}`);
        }

        // Example 5: Get all stock items grouped by category
        console.log('\\n5Ô∏è‚É£ Getting all stock items...');
        
        try {
            const allStock = await tally.stock.getStockItemList({
                activeOnly: true,
                withBalance: false // Include all items, not just those with balance
            });

            console.log(`‚úÖ Total active stock items: ${allStock.count}`);
            
            // Group by parent for better display
            const groupedStock = allStock.data.reduce((groups, item) => {
                const parent = item.parent || 'No Parent';
                if (!groups[parent]) groups[parent] = [];
                groups[parent].push(item.name);
                return groups;
            }, {});

            console.log('üìä Stock items grouped by category:');
            Object.entries(groupedStock).forEach(([parent, items]) => {
                console.log(`   üìÅ ${parent}: ${items.length} item(s)`);
            });

        } catch (error) {
            console.log(`‚ö†Ô∏è  Could not fetch all stock items: ${error.message}`);
        }

        // Example 6: Update a stock item
        console.log('\\n6Ô∏è‚É£ Updating stock item...');
        
        try {
            const updateResult = await tally.stock.updateStockItem('Widget A', {
                alias: 'Widget-A-Premium',
                // Note: Updating base units should be done carefully as it affects calculations
            });

            console.log('‚úÖ Stock item updated:', updateResult.message);
        } catch (error) {
            console.log(`‚ö†Ô∏è  Could not update stock item: ${error.message}`);
        }

        // Example 7: Get stock balance for specific item
        console.log('\\n7Ô∏è‚É£ Getting stock balance...');
        
        try {
            const balance = await tally.stock.getStockBalance('Widget A', {
                asOn: '31-Dec-2023'
                // godown: 'Main Warehouse' // Specify godown if needed
            });

            console.log('‚úÖ Stock balance retrieved:');
            console.log('üìä Opening Balance:');
            console.log('   üì¶ Quantity:', balance.data.openingBalance?.quantity || 0);
            console.log('   üíµ Rate:', balance.data.openingBalance?.rate || 0);
            console.log('   üí∞ Value:', balance.data.openingBalance?.value || 0);
            console.log('üìà Inward Quantity:', balance.data.inwardQuantity || 0);
            console.log('üìâ Outward Quantity:', balance.data.outwardQuantity || 0);
            console.log('üìä Closing Balance:');
            console.log('   üì¶ Quantity:', balance.data.closingBalance?.quantity || 0);
            console.log('   üíµ Rate:', balance.data.closingBalance?.rate || 0);
            console.log('   üí∞ Value:', balance.data.closingBalance?.value || 0);
            console.log('üìÖ As on:', balance.asOn);

        } catch (error) {
            console.log(`‚ö†Ô∏è  Could not get stock balance: ${error.message}`);
        }

        // Example 8: Get stock summary report
        console.log('\\n8Ô∏è‚É£ Getting stock summary...');
        
        try {
            const summary = await tally.stock.getStockSummary({
                group: 'Raw Materials',
                asOn: '31-Dec-2023',
                includeZeroBalance: false
            });

            console.log('‚úÖ Stock summary retrieved:');
            console.log('üìä Total items in summary:', summary.data.totalItems);
            console.log('üí∞ Total value:', summary.data.totalValue);
            console.log('üìà Stock Analysis:');
            console.log('   ‚úÖ Positive stock items:', summary.data.summary.positiveStock);
            console.log('   ‚ùå Negative stock items:', summary.data.summary.negativeStock);
            console.log('   ‚ö™ Zero stock items:', summary.data.summary.zeroStock);

            if (summary.data.items && summary.data.items.length > 0) {
                console.log('üîù Top stock items by value:');
                const sortedItems = summary.data.items
                    .sort((a, b) => b.closingValue - a.closingValue)
                    .slice(0, 5);
                
                sortedItems.forEach((item, index) => {
                    console.log(`   ${index + 1}. ${item.name}: ${item.closingQuantity} ${item.baseUnits} = ‚Çπ${item.closingValue}`);
                });
            }

        } catch (error) {
            console.log(`‚ö†Ô∏è  Could not get stock summary: ${error.message}`);
        }

        // Example 9: Get stock movements
        console.log('\\n9Ô∏è‚É£ Getting stock movements...');
        
        try {
            const movements = await tally.stock.getStockMovements('Widget A', {
                fromDate: '01-Dec-2023',
                toDate: '31-Dec-2023'
                // voucherType: 'Sales', // Filter by specific voucher type
                // godown: 'Main Warehouse' // Filter by specific godown
            });

            console.log(`‚úÖ Found ${movements.data?.length || 0} stock movement(s) for Widget A:`);
            if (movements.data && movements.data.length > 0) {
                console.log('üìã Movement Details:');
                movements.data.forEach((movement, index) => {
                    console.log(`   ${index + 1}. ${movement.date} - ${movement.voucherType} ${movement.voucherNumber}`);
                    console.log(`      üì• Inward: ${movement.inwardQuantity}, üì§ Outward: ${movement.outwardQuantity}`);
                    console.log(`      üíµ Rate: ‚Çπ${movement.rate}, üí∞ Amount: ‚Çπ${movement.amount}`);
                    if (movement.godown) console.log(`      üè™ Godown: ${movement.godown}`);
                });
            } else {
                console.log('   ‚ÑπÔ∏è  No movements found in the specified period');
            }

        } catch (error) {
            console.log(`‚ö†Ô∏è  Could not get stock movements: ${error.message}`);
        }

        // Example 10: Advanced stock operations
        console.log('\\nüîü Advanced stock operations...');
        
        try {
            // Get stock items with positive balance only
            const stockWithBalance = await tally.stock.getStockItemList({
                withBalance: true,
                activeOnly: true
            });

            console.log(`üìä Stock items with positive balance: ${stockWithBalance.count}`);

            // Create a high-value stock item
            const premiumStock = await tally.stock.createStockItem({
                name: 'Premium Component Z',
                parent: 'Raw Materials',
                baseUnits: 'Nos',
                alias: 'PREM-Z',
                openingBalance: {
                    quantity: 10,
                    rate: 1500.00,
                    value: 15000.00
                }
            });

            console.log('‚úÖ Premium stock item created:', premiumStock.stockItemName);

            // Get comprehensive summary for all categories
            const comprehensiveSummary = await tally.stock.getStockSummary({
                includeZeroBalance: true,
                asOn: '31-Dec-2023'
            });

            console.log('üìà Comprehensive Stock Analysis:');
            console.log('   üì¶ Total items:', comprehensiveSummary.data.totalItems);
            console.log('   üí∞ Total inventory value: ‚Çπ', comprehensiveSummary.data.totalValue);

            // Group items by value ranges
            if (comprehensiveSummary.data.items) {
                const valueRanges = {
                    high: 0,    // > ‚Çπ10,000
                    medium: 0,  // ‚Çπ1,000 - ‚Çπ10,000
                    low: 0      // < ‚Çπ1,000
                };

                comprehensiveSummary.data.items.forEach(item => {
                    if (item.closingValue > 10000) valueRanges.high++;
                    else if (item.closingValue > 1000) valueRanges.medium++;
                    else valueRanges.low++;
                });

                console.log('üí∞ Items by value range:');
                console.log(`   üî¥ High value (>‚Çπ10K): ${valueRanges.high} items`);
                console.log(`   üü° Medium value (‚Çπ1K-‚Çπ10K): ${valueRanges.medium} items`);
                console.log(`   üü¢ Low value (<‚Çπ1K): ${valueRanges.low} items`);
            }

        } catch (error) {
            console.log(`‚ö†Ô∏è  Advanced operations error: ${error.message}`);
        }

        console.log('\\n‚úÖ Stock management examples completed!');

    } catch (error) {
        console.error('‚ùå Stock management example failed:', error.message);
        console.log('\\nüîß Troubleshooting:');
        console.log('1. Ensure TallyPrime is running');
        console.log('2. Check if required stock groups exist (Raw Materials, Finished Goods, etc.)');
        console.log('3. Verify API access is enabled in TallyPrime');
        console.log('4. Ensure stock item units are properly configured');
    }
}

